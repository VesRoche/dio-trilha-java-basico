package escola;

public class Aluno {
    private String nome; // Atributos privados
    private int idade;
    private String sexo;

public String getNome() { // nao recebe parametro
    return nome;
}

public void setNome(String newNome) {
    nome = newNome;
}

public int getIdade() {  // nao recebe parametro
    return idade;
}

public void setIdade(int newIdade) {
    this.idade = newIdade;
}

public String getSexo() {  // nao recebe parametro
    return sexo;
}

public void setSexo(String newSexo) {
    this.sexo = newSexo;
}





}
